# inequality index
auc = function(data){
  area = 0
  last = 0
  for(d in data[-1]){
    sub_area = 0.25*(last+d)/2
    area = area + sub_area
    last = d
  }
  return(area)
}

inequality_relative = function(data,ref){
  ref_auc = ref %>% auc
  data_auc = data %>% auc
  output = (ref_auc-data_auc)/ref_auc
  return(output)
}

inequality_absolute = function(data){
  data_auc = data %>% auc
  output = 1-2*data_auc
  return(output)
}

